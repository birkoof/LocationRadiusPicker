import Foundation

/// Represents the unit system used by the picker to represent distance values
public enum UnitSystemType {
    /// Metric unit system, e.g. 100 meters
    case metric
    /// Imperial unit system, e.g. 100 feet
    case imperial
    /// Follow system setting
    case system
}
